            leading schools from Nagpur participated. The theme of the competition was Diversity, Inclusion and Mutual
            Respect.
            <br><br>
            Mr. Vilas Deshpande, Nagar Sanchalak, Vivekananda Kendra, Mr Shrikant Gadge, member of the executive
            committee of International Centre for Cultural Studies, India and Mrs. Sabitha Patel were prominently
            present for the award ceremony.
            <br><br>
            Research Paper Writing, Ted Talk, Poster Making and Slogan Writing were the different competitions that were
            conducted.
      
