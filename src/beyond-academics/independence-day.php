          <h5 class="text-center">INDEPENDENCE DAY CELEBRATIONS AT THE JAIN INTERNATIONAL SCHOOL</h5><br>
          <img src="/images/independence.jpeg" alt="" class="m-auto" style="display: block; margin:auto" width="30%">
          
           <br><br>
           
          <p class="paragraph">
            The Jain International School celebrated the 77th Independence Day of India, with great enthusiasm and
            patriotic fervour. The Chief Guest was Mr. Nitin Nakhat, an entrepreneur and the proud father of the X Board
            topper of the school, Ms. Jaina Nakhat. The programme started with the unfurling of the national tricolour.
            The school celebrated the ‘Make in India’ on this occasion. The Principal Mrs. Anmol Badjatia,
            conceptualized this presentation which was beautifully chorographed by Mrs. Vaishali Khapre (TGT, Dance). Mr
            Mithilesh Pathak (HOD, Hindi and Secondary Co-ordinator) lent his voice to the narration.
            <br><br>
            Prominently present were- Director of The Jain International School Mr. Anuj Badjate, Director Little
            Jewels, Mrs Preeti Badjate, Member, School Management Mr. Raunak Badjate, Chief Executive Director S.B.
            JIMT, Management and Research, Dr. O.S. Bihade, CEO Mr. Sanjeev Agrawal, Principal S.B. Jain Institute of
            Technology, Management and Research, Dr. Sanjay Badjate, Member of the executive committee of International
            Centre for Cultural Studies, India, Shri Shrikant Gadge, Mrs. Sabitha Patel, Headmistress, Little Jewels,
            Mrs. Pooja Gandhi, AGM, Little Jewels, Ms. Deepshikha Nigam and Admin Head, Little Jewels, Mrs. Nagalakshmi
            Krishnakumar.
            <br><br>
            The programme was organized by Mrs. Kiran Joshi, the CCA Incharge.
            Dr. Tanveer Rezvi and Amanjeet Singh (Std. XI) conducted the proceedings and Kritarth Sharma (Std. X)
            proposed the Vote of Thanks.

            The programme ended with the National Song. The management congratulated the Principal Mrs. Anmol Badjatia,
            Vice Principal Mrs. Bhaswati Banerjee, the staff and the students for putting forth a very unique
            celebration.
       </p>
