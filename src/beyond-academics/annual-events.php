            <h2 class="outreach-heading" style="margin-top: 50px;">Youth Exchange Programme,Fukuyama,Japan</h2>

            <p class="paragraph">
              The students of The Jain International School were invited for a Student Exchange Programme to Fukuyama, Japan. This exchange trip was under the aegis of JCI, Nagpur. Ms Rajshree Bhaje, PNP and Chairman JCI Sponsorship committee organised the trip. A delegation of 6 students from Classes V to VII escorted by their teacher coordinator Ms. Pooja Shrivastava (Primary Coordinator) visited Fukuyama.The main aim of the Exchange Programme was to<span class="more"> create awareness about inter-culturalsimilarities and differences between Asian countries and Japan, thereby leading to a better understanding and appreciation of their own culture and nation. This was achieved through various activities held at Fukuyama. Through various hands on activities students were able to communicate with the students of other countries despite language barriers.The students got an exposure to the teaching and learning methodologies in Japanese schools.<br>

              The Exchange Programme included a homestay programme of the students with Japanese families. It taught the students tolerance, discipline and independence and further developed a sense of mutual respect.<br>

              The students also got an opportunity to visit nearby cities of Hiroshima and Tokyo. They visited Hiroshima Peace Museum, Hiroshima Castle, Tokyo Sky Tree and Tokyo Tower amongst other things. The museum exhibits brought home the horrors of the Hiroshima Bombing in World War II. The students learnt the importance of world peace. They also learnt first hand how Japan rose like a phoenix out of the devastation caused by World War II by the dint of hard work and discipline.<br>

              The School Management, the Principal Anmol Badjatia and the parents of the children thanked Ms Rajshree Bhaje for arranging this Exchange Programme.<br>
