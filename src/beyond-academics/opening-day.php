        <h5 class="social-heading">Opening Day</h5>
        <p class="paragraph"> The Jain International School began its academic session 2019-20 with brightly lit faces of the students ready to take any challenge and develop their skills. The students were welcomed by teachers with songs and cards and a motivating speech by the Principal, Mrs. Anmol Badjatia  who spoke on the contribution of all the students and teachers for the twelve years of success of our school and wishing it the best in years ahead. The C.C.A Incharge, Mrs. Kiran Joshi announced the names of the student cabinet and  enthused the students to face all challenges with a  competitive spirit. The teachers were all geared up for the holistic development of each student and make this year a grand success.<br><br>
        <STRONG>Investiture Ceremony </STRONG><br><br>
        The Investiture Ceremony of The Jain International School ushered in a new generation of leaders. The elected cabinet donned the mantle of responsibility for the academic session 2019-20.
        <br><br>
        The ceremony was presided over by the Trustee, Mr. Tushar Badjate.  Mr. Sanjeev Agrawal, CEO, graced the programme with his presence. The dignitaries present included Dr. Sneha Deshpande, HoD, Economics Dept, Nagpur University and Mr. Jyoti Kumar Satija, IRS. The parents of the Cabinet Members, the Captains and Vice-Captains were also present on the occasion.
        <br><br>
        The Trustee, Mr. Tushar Badjate, delivered a stirring speech which inspired the students to put their best foot forward.
        <br><br>
        The programme commenced with a shloka and a dance invoking the blessings of Lord Ganesh that was beautifully executed with the guidance of Mr. Vivek Khapre, Mrs. Vaishali Khapre and Mr. Umeshwar Barapatre.
        <br><br>
        The School Cabinet, Captains and Vice - Captains were awarded sashes and badges by Mr.Tushar Badjate and Mr. Sanjeev Agrawal in a solemn ceremony. The oath was administered by the Principal, Mrs. Anmol Badjatia, wherein the newly elected cabinet pledged to dispense their duties to the best of their abilities.
        <br><br>
        The House Mistresses & Organizing Committee were also awarded badges by the Principal, Mrs. Anmol Badjatia.
        <br><br>
        In keeping with tradition, the Head Boy, Vardhan Mahajan and the Head Girl, Aprajita Sharma addressed the audience and shared their game plan for the coming session.
        <br><br>
        Mr. Tushar Badjate declared the house activities open for the new session.
        <br><br>
        The programme was coordinated by the CCA Incharge, Mrs. Kiran Joshi. Mrs.Anupama D’Souza, Senior Secondary Coordinator compered the programme.
        <br><br>
        The programme concluded with a rendition of the School Anthem. The members of the Management and the Principal, Mrs.Anmol Badjatia congratulated the new cabinet members & wished them success in their future endeavours.
        <br><br>
          <STRONG>Fast Track Your Future</STRONG><br><br>
          The Jain International School continued its quest for excellence with its innovative and far-sighted career guidance unique programme for its students - “FAST TRACK YOUR FUTURE”. The programme, which is an annual feature, aims to help students gain a better understanding of their academic interest, job opportunities in various field and career options available. It helps students identify their career goals at the secondary level and gives them an opportunity to spend a few working days with an expert in their chosen field.
          <br><br>
          This unique programme, the brain child of Mr. Anuj Badjate, the Director of the school, completed its eighth year of existence. The students were not only given a chance to learn about the experiences of experts, but also got to understand the hard work, discipline and commitment required to reach the heights of success in their chosen field.  An extensive month-long exercise saw the responses being shortlisted and selected, with mentors being allotted. The mentors were Mr. Jyoti Kumar Satija (IRS), Mr.Hudson Misal (Aviation Industry), Mr. Romil Jain (Lawyer, High Court), Prof. Sneha Deshpande (HoD, Economics, Nagpur University) &  Aparna Samudrakar (Dept. of Economics, Nagpur University), Mrs. Nidhi Gandhi (Fashion Industry), Mr. Amit Tiwari (Software Developer, Director, Flying Penguins), Dr. Kameswar Rao (General Manager – RRSC, ISRO, Nagpur), Mr. Shetank Bhandari (National Level Tennis Player & Entrepreneur), Dr.Jaspal Arneja (Cardiologist, Arneja Hospital)  and Dr. Rupali Belsare (Veterinary Doctor).
          <br><br>
          At the culmination event of this programme the mentors were invited to share their views with the children of the school. Mr Jyoti Kumar Satija and Prof.Sneha Deshpande addressed the students and gave them valuable insights about coping with life’s challenges. Their advice was taken to heart by the appreciative audience which was truly inspired by their anecdotes.
          <br><br>
          The initiative proved to be a learning experience for the students. It not only boosted their confidence, but prepared them for future endeavours. The project demonstrated the value of career guidance as a vehicle for maximizing the contribution of work related learning to the student experience.
          <br><br>
          The CCA Incharge Mrs. Kiran Joshi was commended by the Principal Mrs. Anmol Badjatia and the Management on the success of the project.
          <br><br>
          <STRONG>INDEPENDENCE DAY CELEBRATION AT JAIN INTERNATIONAL SCHOOL</STRONG><br><br>
          The Jain International School celebrated the 73rd India’s Independence with patriotic fervour and a well coordinated programme.
          <br><br>
           The School Cabinet escorted the dignitaries to the flag post. The tri-colour was unfurled by the Chief  Guests – Sgt. Naresh Sachan and Mrs. Shobhana Sachan (parents of 2018-19 academic session topper – Ayush Sachan), along with the Director of the school – Mr. Anuj Badjate and other members of the Management.
          <br><br>
         A cultural programme was organized which included patriotic songs sung by the school choir, a musical performance from the students of S.B.Jain Institute of Technology, Management and Research, an inspiring speech by Saniya Khan (Std. XI - Commerce),a dance performance by the middle and secondary students, poem recitations by Naira Chauhan, Sparsh Meshram (Kindergarten section), Devendra Pilkhane (College), karate and self-defense techniques and a speech by the Chief Guest who apprised the students with India’s Freedom Struggle, with special reference to the revolt of 1857. The vote of thanks was proposed by Bhagyashree Nikose(Discipline Prefect) (Std. X). The programme was well coordinated by CCA incharge- Mrs.Kiran Joshi & compered by Mrs. Ishmeeta Warne, Ananya Singh(Std. X) and Ryan Bang (Std. X)
          <br><br>
          The School Management, Principal, Mrs. Anmol Badjatia appreciated the efforts of all the students and the teachers for putting up a wonderful show.
        </p>
