                                        <div class="entry-post">
                                            <p>Shyness is a common but misunderstood emotion. Shyness can be defined as a state or quality of being nervous or timid in the company of other people. Shyness is a universal feeling and can be argued to have evolved as an adaptive mechanism used to help individuals cope with social stimuli. Everyone has felt ambivalent or self-conscious in a new social situation. However, there are times when shyness can interfere with optimal social development and can restrict children’s learning. Children may be vulnerable to shyness at particular developmental points.</p>
                                            <p>Perhaps, when a child is born, the last thing the parents think about is whether their child is shy or not. Parents pray for their children to be healthy and normal. They keep track of the child’s height and weight and record every milestone of the first words and steps the child achieves. This increased emphasis on motor development, language skills, and learning tends to divert the attention of parents, teachers, and even child psychologists away from the social and emotional growth of children.</p>
                                            <p>Researchers have shown that some children are shyer than others. However, all shy children get stuck in a vicious cycle that holds them from connecting with other kids. When shy kids are around other kids, they feel like outsiders looking in. These children feel uncomfortable in social settings and avoid interacting with their peers.</p>
                                            <p>Subsequently, this leads to less practice talking and playing with other kids. Thus, there is decreased opportunity to develop social skills such as having conversations, resolving arguments, taking turns, and figuring out fun things to do together. Imagine the pain that parents might feel as they watch a shy child desperately wanting to be accepted by other children yet don’t know what steps to take to gain their approval. 
To prevent a child from experiencing greater loneliness in their adolescence due to their shyness, it is crucial to adopt specific strategies that will benefit the child to overcome his/her shyness from an early age.
</p>

                                            <ul>
                                                <li><b>1. Know your Child: </b>
Be sensitive to the child’s interests and feelings. This will build a strong relationship with the child and show that you respect the child. This makes the child more confident and less inhibited.
</li>
                                                <li><b>2. Avoid labelling your child:</b>
<i>“He’s our shy one.”</i> Labels often define who we are and who we believe we can become. A label that you create might haunt the child throughout his life.
 </li>
                                                <li><b>3. Build Self-Esteem and Confidence:</b>
Shy children might have negative self-images and might feel that they will not be accepted. It is important to reinforce shy children for demonstrating skills and encourage their autonomy. Praise them often.</li>
<li><b>4. Develop Social Skills:</b>
Opportunities for play with young children in a one-on-one situation may allow shy children to become more assertive. Play with new groups of peers permits shy children to make a fresh start and achieve a higher peer status.</li>
<li><b>5. Allow the Shy Child to Warm up to New Situations:</b>
Pushing a child into a situation that he or she sees as threatening is less likely to help the child build social skills. Help the child feel secure and provide interesting materials to lure him/her into interactions.</li>
<li><b>6. Encourage the Child to Identify Three Classmates:</b>
The child should consider these classmates to be fun to play with. Maybe you can arrange an after-school trip to a park or ice cream shop to see if they make a connection.</li>
<li><b>7. Ask the teacher to give your child some responsibility:</b>
 Some less outgoing kids tend to blossom if they feel special and important.</li>
<li><b>8. Remember That Shyness is Not All Bad:</b>
 Not every child needs to be the focus of attention. According to Jones, Cheek, and Briggs (1986), some qualities of shyness, such as modesty and reserve, are favorable. As long as the child doesn’t seem excessively uncomfortable or neglected around others, drastic interventions are unnecessary.</li>
                                            </ul>
                                            <p>Shyness in children is common. These strategies help prevent a child's shyness from interfering with their social development and learning. The earlier you start, the better your chance of success. Blog written by Ms. Vidhi Maheshwari holding a degree in counselling psychology from Columbia University, New York. She started her own practice, MindUnwind, with the aim to make mental health services more accessible and affordable. She works with children, adolescents, adults and families.  

