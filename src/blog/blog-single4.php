<br>I pray to them to keep me ever happy and full of glee,
<br>Then once again as I sat back and closed my eyes;
<br>I think of all those things holding me by.....
<br>All those cutting refusals, spicy taunts, jabbering comments that came by,
<br>All those unworthy disheartening conversations, lonely moments and heartbreaking lies.
<br>All those hands that were ready to pull me back as soon as I started dreaming high,
<br>About those wings that I always longed for to help me fly.
<br>About those people who appeared to be astringently sweet,
<br>Of all those haunted moments that would jitter my beauteous sleep.
<br>Then again as I sat back to myself and closed my eyes;
<br>I saw all those smiles and blessings cuddling me by.
<br>I saw my angels holding me tight, standing by my side,
<br>Urging me to fasten my seat belt and get going on my dream ride.
<br>Through the journey I realize, people, places and things that are just part and parcel of a big life plan 
