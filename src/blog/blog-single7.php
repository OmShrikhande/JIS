                                            <p>Once on a Sunday morning I was having my breakfast, suddenly I heard a ring on my father’s mobile phone. I gave the mobile to him; there was a call from the blood bank as they had an urgent requirement of blood. I went to the blood bank with him. He is a regular blood donor; he donates his blood and platelets every 3 months. I asked my father “why you donate blood?” He explained why we should donate blood:</p>

                                            <ol>
                                                <li>	Giving blood saves life
</li>
                                                <li>	Giving blood can reveal potential health problems
 </li>
                                                <li>	Giving blood can reduce harmful iron stores
</li>
<li>	Giving blood may lower your risk of suffering a heart attack</li>
<li>	Giving blood may reduce your risk of developing cancer</li>
<li>	Giving blood can help your liver stay healthy</li>
<li>	Giving blood can help your mental state</li>
                                            </ol>
                                            <p>I found it very interesting and I started researching about it. I got to know that in the 1800’s a great Gynaecologist James Blundell performed the first successful transfusion of human blood to a patient for the treatment of postpartum haemorrhage and this is how the world came to know about blood donation. The study of blood is called Haematology. There are four components of blood :</p>
                                            <ol>
                                                <li>White blood cells (WBC)</li>
                                                <li>Red blood cells (RBC)</li>
                                                <li>Plasma</li>
                                                <li>Platelets</li>
                                            </ol>
                                            <p>There are eight blood groups which are:<br>
A+/ A positive, A- / A negative, B+/B positive, B-/B negative, O+/O positive, O-/ O negative, AB+/ AB positive, AB-/ AB negative. Any one, of these blood groups is found in our body. The most common type, O+ or O positive is found in 37.5% of the population and can be used in transfusions with any individual who has a positive blood type. The least common, AB-, makes up 0.6% of the population, while the universal donor O- is only found in 6.6% of the population. This makes blood collection and distribution in our healthcare system more challenging and that is why we need constant blood donations. Every year the blood donation day is celebrated on 14th June.
</p>
<p>After gaining this knowledge, I was thinking how to promote and spread the benefits of donating blood. I told my father about my curiosity, so he connected me with one of his friends who is a doctor. She actively participates in many blood donation camps. She explained how this blood helps people, who are sick, those who have met with an accident, and who go in for an operation. It helps them to recover and lead a healthy life. Now, along with my doctor aunt I am also actively involved in this mission and support the cause which helps to save lives. I speak to my friends and their parents and urge them to start donating blood for a good cause.  I also came to know that children cannot donate blood till a certain age is reached and I am excited and waiting for my 18th birthday when I will donate blood for the first time.</p>
                                            <p><b><u>Moral: blood donation is the responsibility towards ourselves and others.</b></u>


