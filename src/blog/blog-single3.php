                                            <p>Dear reader, what’s the first thing that comes to your mind when you see a flower blooming or an aeroplane flying high in the sky or may be when you are using your laptop or smartphone? Well…. The first and the only thing that comes to my mind is SCIENCE!
<br>The modern English word ‘science’ is related to the Latin word ‘scientia’ which means ‘knowledge.’ It seems preposterous to us today that people once thought that the Earth was flat. Who could have possibly thought of our planet as a giant disk with the stars and heavens above, and boulders, tree roots, and other things below? Greek philosophers referred to a spherical Earth as early as the 6th century BCE. They observed that the moon appeared to be a sphere and therefore inferred that Earth might also be spherical. Two hundred years later, in the 4th century BCE, the Greek philosopher Aristotle observed that the shadow of the Earth on the Moon during a lunar eclipse is always curved, thus providing some of the first evidence that Earth is spherical. Aristotle, Eratosthenes, and Strabo didn't call themselves scientists, yet they were using the process of science by making observations and providing explanations for those observations.
<br>So, the question is… What is Science? 
<br>Science consists of two things: knowledge and the process by which that knowledge is produced. A scientific way of thinking is something that anyone can use, at any time, whether or not they are in the process of developing new knowledge and explanations.
<br>But hey! Wait a minute! Why are we talking about this? What is the importance of science for us?
<br>It is because science is the only methodical, rational investigation into how the universe functions. It is one of humanity's blessings that has greatly contributed to enhancing human standard of living. Science is everywhere and has complete power over every part of our existence. Every aspect of our existence revolves around science.
<br>Einstein once said “All our science measured against reality, is primitive and childlike - and yet is the most precious thing we have.”
<br>Science has been extremely beneficial to people. The investigation of environmental issues by man, a rational being, has been peculiar and has led to several discoveries around the world. Science is the study of the environment. Many branches of science, including physics, chemistry, and biology, are researched, as well as creatures, chemicals, the force, the earth, plants, and other topics.
<br>Science has invented different types of machines that are very useful for us in our daily life. Everything that the modern man uses from morning till night has been manufactured by one machine or the other. Computers have made each and everything easy like calculations and other stuff beyond our imagination. Electricity is the greatest invention of man. It serves us in hundreds and thousands of ways. It runs our trains, mills and factories. It gives us cool air and entertains us through Cinema, TV and radio. 
<br><br>
<br>The greatest blessings of science are its inventions in the field of medicine and surgery. Human pain and treatment of different types of diseases that don’t have any treatment available in the early times that have now become very easy.
<br>Landing on a moon was just an imagination of a man few years back. But by the help of modern science man has also done this impossible work.
<br>Science is a great blessing for mankind. Its utilities in everyday life are unlimited. But the inventions of science are being distorted in many different ways. Instead of using the aero-planes for travel, we use them to drop bombs. In this way, this blessing has turned into a curse, and it is feared that one day science will destroy humanity.
<br>Science is a good servant, but man must remain master. To conclude we can say that science is the magical genie of today. It can do wonders for us if it is used carefully.
<br><br>
