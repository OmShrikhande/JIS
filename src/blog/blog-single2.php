कितना अच्छा होता न जब पेड़ – पौधे भी बोल पाते l वसंत की बहार एवं खुशियों को गाकर हमें सुनाते और पतझड़ के दर्द को बयाँ करते l हम भी उनकी खुशियों में शामिल होते और उनके दर्द को सुनकर सहानुभूति  प्रकट करते l तब शायद मनुष्य इतना स्वार्थी नहीं होता और अपनी सुविधा के लिए पेड़ पौधों का भक्षक न बनता l 
<br>काश ! ऐसा हो पाता....ज़रा कल्पना कीजिए कि वृक्ष अपनी आपबीती बताने के लिए मनुष्य के नाम एक पत्र लिखता है l इस पत्र में वृक्ष अपनी वो हर बात बयाँ करता है जो उसपर बीत रही हैl आइए पढ़ते हैं कि वृक्ष का पत्र देशवासियों के नाम.....
<br><br><b>प्यारे देशवासियों !</b>
<br><br>मैं आपका अभिनंदन करूँ, आपको आशीर्वाद दूँ ,आपकी प्रार्थना करूँ , विनती करूँ या आपका तिरस्कार करूँ l चलिए जाने दीजिए इसका फैसला परमपिता परमेश्वर पर छोड़ देते हैं l
<br>पत्र प्रारंभ करने से पहले मैं अपना परिचय देना चाहता हूँ l सृष्टि की अनुपम कृति, इस धरा का श्रृंगार , मनुष्य के प्राणवायु का स्रोत , सूर्य की तपन से शीतलता प्रदान करने वाला , मूसलाधार वर्षा से भू-रक्षण करने वाला धरती माँ का बेटा- एक वृक्ष हूँ ; सघन परिवार  का एक छोटा सा सदस्य l मैं अपने पूरे परिवार की ओर से आप सभी से आग्रह करता हूँ कि आप मेरे महत्त्व को समझें , मेरी पीड़ा को सुने, मेरे दर्द को महसूस करें  एवं मेरी खुशियों का ख्याल रखें l उम्मीद करता हूँ कि आपसब मेरी बात अवश्य सुनेंगे l 
<br>कहते हैं कि एकता में बल है l परिवार के सदस्य मिलजुलकर रहें तो कठिन से कठिन समस्या का समाधान असानी से हो जाता है l मेरे परिवार में मेरे जैसे अनेक सदस्य मिलजुलकर रहते हैं l शेर, चीता , बाघ, भालू, हिरन, भालू, हाथी इत्यादि प्राणियों के लिए तो वन घर है इसलिए तो इन्हें वन्य प्राणी कहते हैं l हमसब मिलकर न सिर्फ इनका संरक्षण करते हैं बल्कि इन्हें विपदाओं से बचाते भी हैं l अब आपको लग रहा होगा कि हम आपके काम आते भी हैं या नहीं l हम आपके लिए कितने उपयोगी हैं इस बात की कल्पना करकर ही आप रोमांचित हो उठेंगे l हम दूषित वायु ग्रहण कर आपको प्राणवायु देते हैं, हमारे सभी सदस्य मिलकर बरसात के दिनों में मिट्टी के कटाव को रोकते हैं जिससे मिटटी की उर्वरा शक्ति कायम रहती है , वह उपजाऊ बनी रहती है और उपजने वाले अन्न का उपभोग करके आपका जीवन चलता है l प्राचीनतम चिकित्सा पद्धति आयुर्वेद ने हम में से प्राप्त जड़ी बूटियों द्वारा ऐसी –ऐसी औषधियों का निर्माण किया है जो भयानक से भयानक रोगों से मुक्ति दिला सकता है l हमारी लकड़ियों का उपयोग इंधन से लेकर भवन निर्माण तक में होता है l हमारी उपयोगिता का अनुमान तो अपने लगा ही लिया होगा l 
<br>इस धरा में जब आपका अस्तित्व नहीं था तब हमारी संख्या असंख्य थी l मनुष्य की उत्पत्ति होने पर भी हम प्रसन्न ही थे क्योंकि हम आपके काम आ सकते थे, पर हमें क्या मालूम था कि आपकी उत्पत्ति हमारे विनाश का कारण बन जाएगी l जैसे- जैसे धरा पर आपका परिवार बढ़ता गया वैसे-वैसे हमारा परिवार घटता गया l वन्य प्राणी लुप्त होते जा रहे हैं l आप अपने पैरों पर खुद कुल्हाड़ी मारकर प्रसन्न हो रहे हैं ; आपकी इस नासमझी के कारण प्राकृतिक सम्पदा विनाश के कगार पर पहुँच  गई है और मेरे ह्रदय से एक ही आवाज़ आ रही है –
<br><br><b>“पथरीले रास्ते हुए , कंक्रीट के वन,                                                                                                          कहाँ खो गया  आदमी , पूछे मेरा मन l” </b>
<br>पर्यावरण संरक्षण आज विश्व के लिए सबसे बड़ी चुनौती है l बेशुमार बढ़ती जनसंख्या एवं भौतिकता की अंधी दौड़ स्तिथियाँ और भी विषम कर दी हैं l नैतिकता को तिलांजलि देकर भौतिक संपन्नता को लोगों ने सब कुछ मान लिया है l हरे भरे वृक्षों को लगातार काटा जा रहा है, पृथ्वी का तापमान लगातार बढ़ने से ‘भूमंडलीय उष्मीकरण ‘ बढ़ता ही जा रहा है l बहुत पहले प्रकृति पूजा को महत्त्व देकर आपके पूर्वजों ने अपने वर्तमान को तो सुंदर बनाया ही था , अपनी भावी पीढ़ी को भी सुंदर एवं समृद्ध धरा सौंपी थी l सूर्य, जल, वायु, प्रकाश एवं पृथ्वी की आराधना की जाती थी ;इन सबको देवता एवं शक्ति मानकर इनका संरक्षण किया जाता था l पर आज या हमारे लिए आराध्य न होकर उपभोग की वस्तु बनकर रह गए हैं l 
<br>प्रदूषण की समस्या ने पूरे विश्व को झकझोर दिया है l मैं आज दर्द से तड़प रहा हूँ l आपसब ने मिलकर मेरी मान क्या अपनी माँ को लहुलूहान कर दिया है l जब आप एक-एक करके मेरे परिवार के सदस्यों को क्षत –विक्षत करते हैं तब धरती माँ का सीना छलनी हो जाता है l जब आप अपने लिए मकान बनाते हैं तब वही हमारे लिए शमशान बन जाता है l जब आप कारखाने बनाते हैं तो वही हमारे लिए काल बन जाता है l 
<br>आप यह क्यों नहीं समझते कि यदि हम नहीं रहेंगे तो आपका भी अस्तित्व धीरे – धीरे समाप्त हो जाएगा l हमारी रक्षा का दायित्व ‘राष्ट्रीय वन विभाग ‘ के सदस्यों तक सीमित न होकर आम आदमी का भी होना चाहिए क्योंकि जीवन पर सबका अधिकार है l आपसब प्रण कीजिए कि हर देशवासी वर्ष में दस –दस  वृक्ष (पौधे) लगाएगा एवं उसे संरक्षण प्रदान करेगा l आप यकीन मानिए कि तब जंगल का वर्चस्व बना रहेगा , कहीं अकाल नहीं पड़ेगा, पृथ्वी आग नहीं उगलेगी, न अतिवृष्टि होगी न अनावृष्टि , भूमि बंजर होने से बच जाएगी एवं चारों ओर खुशियाँ ही खुशियाँ छा जाएगी l चलते-चलते मैं यही कहना चाहता हूँ –
